        ownerIdProperty.put("type", "string");
        ownerIdProperty.put("required", true);
        ownerIdProperty.put("description", "The ID of the owner.");
        properties.put("ownerId", ownerIdProperty);
        parameters.put("properties", properties);
        
        MCPServer.ToolDefinition definition = new MCPServer.ToolDefinition(
            "delete_owners_owner_id",
            "Delete an owner by ID",
            parameters
        );
        
        return new MCPServer.Tool(definition, getDelete_Owners_Owner_IdHandler(config));
